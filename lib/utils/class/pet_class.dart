class Pet {
  final String petId;
  final String ownerId;
  final String petImageUrl;
  final String name;
  final String age;
  final String weight;
  final int type;
  final int sterilizationStatus;
  final int gender;
  final int breed;

  Pet({
    required this.petId,
    required this.ownerId,
    required this.petImageUrl,
    required this.name,
    required this.age,
    required this.weight,
    required this.type,
    required this.sterilizationStatus,
    required this.gender,
    required this.breed,
  });
}
